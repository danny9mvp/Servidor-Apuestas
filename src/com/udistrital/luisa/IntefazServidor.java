    private void btnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarActionPerformed
        try {
            serverSocket = new ServerSocket(9000);            
            try {
                socket = serverSocket.accept();
                JOptionPane.showMessageDialog(null, "Cliente conectado.","Informaci√≥n",JOptionPane.INFORMATION_MESSAGE);
            } catch (IOException ex) {
                Logger.getLogger(IntefazServidor.class.getName()).log(Level.SEVERE, null, ex);
            }
            Thread hiloEscucha = new Thread(new Runnable() {
                public void run() {
                    String entrada = "";
                    DataInputStream dis = null;                                           
                        try {
                            dis = new DataInputStream(socket.getInputStream());
                            while(true){
                                entrada = dis.readUTF();
                                areaMensaje.append(entrada+"\n");
                            }
                        } catch (IOException ex) {
                            Logger.getLogger(IntefazServidor.class.getName()).log(Level.SEVERE, null, ex);
                        } 
                    
                }
            });
            hiloEscucha.start();
        } catch (IOException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnIniciarActionPerformed

    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        DataOutputStream dos=null;
        try {            
            dos = new DataOutputStream(socket.getOutputStream());
            dos.flush();
            dos.writeUTF(this.txtEnviar.getText());             
            System.out.println("Mensaje Enviado");
        } catch (IOException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnEnviarActionPerformed

